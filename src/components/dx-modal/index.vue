<template>
  <transition name="modal" v-if="currentValue">
    <div class="modal-mask" @click="close">
      <div class="modal-wrapper">
        <div class="modal-container">
          <div class="modal-header">
            <slot name="header">
              default header
            </slot>
          </div>
          <div class="modal-body">
            <slot name="body">
              default body
            </slot>
          </div>
          <div class="modal-footer">
            <slot name="footer">
            </slot>
          </div>
        </div>
      </div>
    </div>
  </transition>
</template>

<script>
  export default {
    props: {
      value: {
        type: Boolean,
        default: false
      }
    },
    data () {
      return {
        currentValue: false
      }
    },
    created () {
      this.currentValue= this.value || false
    },
    watch: {
      value (val) {
        this.currentValue = val
      },
      currentValue (newVal) {
        this.$emit('input', newVal)
      }
    },
    methods: {
      close () {
        this.currentValue = false
        this.$emit('close')
      }
    }
  }
</script>

